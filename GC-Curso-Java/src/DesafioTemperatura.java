public class DesafioTemperatura {
    static void main() {
        double temperaturaCelcius = 30.4;
        double temperaturaFahrenheit = (temperaturaCelcius * 1.8) + 32;
        System.out.println("Temperatura em fahrenheit: " + temperaturaFahrenheit);
        int temperaturaFahrenheitInt = (int) temperaturaFahrenheit;
        System.out.println("Temperatura Fahrenheit: " + temperaturaFahrenheitInt);
    }
}
